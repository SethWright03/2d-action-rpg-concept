import pygame
import object

x = 0
y = 0

wallTile = object.WallTile(x,y)

def genLevel(levelData):
    walls = pygame.sprite.Group()
    spawner = pygame.sprite.Group()
    for y, row in enumerate(levelData):
        for x, tile in enumerate(row):
            if tile == 1:
                wallTile = object.WallTile(x * 50, y * 50)
                walls.add(wallTile)
            if tile == 2:
                spawnTile = object.skeleSpawner(x * 50, y * 50)
                spawner.add(spawnTile)
    return walls, spawner

